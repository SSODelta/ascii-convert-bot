	public static char getBestChar(double density){
		
		//Find the one with lowest deviation
		double dd=100;
		int index=-1;
		for(int i=0;i<ALPHA.length;i++){
			double dx = Math.abs(ALPHA[i]-density);
			if(dx<dd){
				
				index=i;
				dd=dx;
			}
		}
		
		//return the lowest deviating
		return convertLetter(index);
		
		
	}
	
